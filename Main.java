//1.2 Write a Java program to display the default value of all primitive data types of Java.

public class Main {
    public static void main(String[] args) {
        System.out.println("Default values are.....");
        boolean val1 = false;
        System.out.println("boolean = " + val1);
        double val2 = 0;
        System.out.println("double = " + val2);
        float val3 = 0;
        System.out.println("float = " + val3);
        int val4 = 0;
        System.out.println("int = " + val4);
        long val5 = 0;
        System.out.println("long = " + val5);
        String val6 = "null";
        System.out.println("String = " + val6);
    }
}